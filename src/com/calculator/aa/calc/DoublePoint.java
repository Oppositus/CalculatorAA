package com.calculator.aa.calc;

public class DoublePoint {
    private final double x;
    private final double y;

    public DoublePoint(double _x, double _y) {
        x = _x;
        y = _y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
